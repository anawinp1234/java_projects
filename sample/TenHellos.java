public class TenHellos {
    public static void main(String[] args) {
        for (int i = 1; i < 2; i++) {
            System.out.println(i + "st Hello");
        }        
        for (int i = 2; i < 3; i++) {
            System.out.println(i + "nd Hello");
        }  
        for (int i = 3; i < 4; i++) {
            System.out.println(i + "rd Hello");
        }    
        for (int i = 4; i < 11; i++) {
            System.out.println(i + "th Hello");
        }
    }
}
